// Assignment Operator

let x = 10;
let y = 20;

x = x+y;
console.log("x= ",x);

x += y; // x = 30  y = 20
console.log(x);

x = x-y; // x = 50  y = 20
console.log("x = ",x);

x -= y; // x = 30  y = 20
console.log(x);

let a = 2;
